# 🎯 String Operations in Python

# 🔗 Concatenation
str1 = "Hello"
str2 = "World"
print(str1 + str2)  # Join

# 🌀 Repetition
str3 = "Hi"
print(str3 * 3)  # Repeat

# 🔍 Membership
str4 = "Python Programming"
print("Python" in str4)  # Check

# 📏 Length
print(len(str4))  # Count

# 🔡 Case Conversion
print(str4.lower())  # Lower
print(str4.upper())  # Upper

# 🧵 Split
print(str4.split())  # Divide

# 🧹 Strip
str5 = "   Trim Me   "
print(str5.strip())  # Clean

# 🧩 Replace
print(str4.replace("Python", "Java"))  # Swap

# 🔎 Find
print(str4.find("Program"))  # Locate

# 🪢 Join
words = ["Code", "More"]
print(" ".join(words))  # Merge

# 🔢 Count
str6 = "Repeat Repeat Repeat"
print(str6.count("Repeat"))  # Frequency

# 🚦 Startswith / Endswith
print(str4.startswith("Python"))  # Begin
print(str4.endswith("Programming"))  # End